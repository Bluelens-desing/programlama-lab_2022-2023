#include<stdio.h> 
#include<math.h>
int main()
{
	
	/*
	ceil:bir üst sayýya yuvarlýyor
	floor:bi alt sayýya yuvarlýyor 
	sqrt:karekökünü alýyor 
	pow:üstlü ifadesini hesaplýyor 
	abs:mutlak deðer hesaplýyor
	*/
	printf("%f\n",ceil(4.3));
	printf("%f\n",floor(4.3));
	printf("%f\n",sqrt(4));
	printf("%f\n",pow(4,3));
	printf("%d\n",abs(-4));
}
